package jfc;

public class CoppiaInteri {
    private int primo;
    private int secondo;

    public CoppiaInteri(int i1, int i2) {
        this.primo = i1;
        this.secondo = i2;
    }

    public int getPrimo() {
        return primo;
    }

    public void setPrimo(int primo) {
        this.primo = primo;
    }

    public int getSecondo() {
        return secondo;
    }

    public void setSecondo(int secondo) {
        this.secondo = secondo;
    }
}
