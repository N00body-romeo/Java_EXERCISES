package jfc;

public class CoppiaStringhe {
    private String s1;
    private String s2;

    public CoppiaStringhe(String s1, String s2) {
        this.s1 = s1;
        this.s2 = s2;
    }

    public String getPrimo() {
        return s1;
    }

    public String getSecondo() {
        return s2;
    }

    public void setPrimo(String s1) {
        this.s1 = s1;
    }

    public void setSecondo(String s2) {
        this.s2 = s2;
    }
}
